package vo;

public class UrlVO {

	int url_code;
	int url_board_no;
	String url_path;
	
	public int getUrl_code() {
		return url_code;
	}
	public void setUrl_code(int url_code) {
		this.url_code = url_code;
	}
	public int getUrl_board_no() {
		return url_board_no;
	}
	public void setUrl_board_no(int url_board_no) {
		this.url_board_no = url_board_no;
	}
	public String getUrl_path() {
		return url_path;
	}
	public void setUrl_path(String url_path) {
		this.url_path = url_path;
	}
	
	
}
